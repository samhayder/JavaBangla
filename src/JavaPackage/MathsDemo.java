package JavaPackage;

import java.util.Scanner;

public class MathsDemo {
    public static void main(String[] args) {

        int absoluteValue = Math.abs(-2);
        System.out.println("Absolute Value: " + absoluteValue);

        double square = Math.sqrt(25);
        System.out.println("Square Value: " + square);

        double power = Math.pow(2,8);
        System.out.println("Power: " + power);

        double pi = Math.PI;
        System.out.println("PI: " + pi);

        double log = Math.log(5);
        System.out.println("Log: " + log);

        int max = Math.max(2,4);
        System.out.println("Max:" + max);

        int min = Math.min(4,1);
        System.out.println("Min: " + min);

        double ceiling = Math.ceil(5.2);
        System.out.println("Ceiling: " + ceiling);

        double floor = Math.floor(-6.9);
        System.out.println("Floor: " + floor);

        


    }
}
